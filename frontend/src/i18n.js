let listeners = [];
function emit() { listeners.forEach((fn) => fn()); }
function subscribe(cb) { listeners.push(cb); return () => { listeners = listeners.filter((x) => x !== cb); }; }
function getSnapshot() { return localStorage.getItem("lang") || "no"; }

export function setLang(lang) {
    localStorage.setItem("lang", lang);
    emit();
}

function interpolate(str, vars) {
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

export function useLang() {
    const lang = useSyncExternalStore(subscribe, getSnapshot, getSnapshot);
    const t = (key, vars) => interpolate(translations[lang]?.[key] ?? translations.no[key] ?? key, vars);
    return { lang, t, setLang };
}
